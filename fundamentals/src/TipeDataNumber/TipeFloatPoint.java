package TipeDataNumber;

public class TipeFloatPoint {
    public static void main(String[] args) {
        // float : nilai float hanya dapat menampung enam atau tujuh digit setelah koma desimal
        float myFloat = 23.99999F ;
        System.out.println(myFloat);

        //double : nilai Double dapat menampung 15 digit setalah koma desimal
        double myDouble = 99.1762890178D ;
        System.out.println(myDouble);

        // Contoh
        double value = 99.123;
        System.out.println(value);

        // Angka di belakang value hanya menandakan tipe data
    }
}
