package TipeDataNumber;

public class TipeInteger {
    public static void main(String[] args) {
        // byte : Tipe data byte dapat menyimpan bilangan bulat dari -128 hingga 127
        byte myByte = 100;
        System.out.println(myByte);

        // short : Tipe datanya shortdapat menyimpan bilangan bulat dari -32768 hingga 32767
        short myShort = 5000;
        System.out.println(myShort);

        // int   : Tipe datanya intdapat menyimpan bilangan bulat dari -2147483648 hingga 2147483647
        int myInt = 10000;
        System.out.println(myInt);

        // long  : Tipe data longdapat menyimpan bilangan bulat dari -9223372036854775808 hingga 9223372036854775807.
        long myLong = 900000L ;
        System.out.println(myLong);

    }
}
